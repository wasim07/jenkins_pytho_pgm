def add(x,y):
    return x+y

def subtract(x,y):
    return x-y

def multiply(x,y):
    return x*y

def divide(x,y):
    return x/y

print("sum of two number is ",add(10,20));
print("sub of two number is ",subtract(10,5));
print("mul of two number is ",multiply(10,2));
print("div of two number is ",divide(100,20));
